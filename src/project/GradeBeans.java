package project;

public class GradeBeans {
	private int studentID;
	private String name;
	private int grade;
	private int class_unique_number;
	private int division;
	private int attendance;
	private int assignment1;
	private int assignment2;
	private int midterm_exam;
	private int final_exam;
	
	public int getStudentID() {
		return studentID;
	}
	public void setStudentID(int studentID) {
		this.studentID = studentID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getGrade() {
		return grade;
	}
	public void setGrade(int grade) {
		this.grade = grade;
	}
	public int getClass_unique_number() {
		return class_unique_number;
	}
	public void setClass_unique_number(int class_unique_number) {
		this.class_unique_number = class_unique_number;
	}
	public int getDivision() {
		return division;
	}
	public void setDivision(int division) {
		this.division = division;
	}
	public int getAttendance() {
		return attendance;
	}
	public void setAttendance(int attendance) {
		this.attendance = attendance;
	}
	public int getAssignment1() {
		return assignment1;
	}
	public void setAssignment1(int assignment1) {
		this.assignment1 = assignment1;
	}
	public int getAssignment2() {
		return assignment2;
	}
	public void setAssignment2(int assignment2) {
		this.assignment2 = assignment2;
	}
	public int getMidterm_exam() {
		return midterm_exam;
	}
	public void setMidterm_exam(int midterm_exam) {
		this.midterm_exam = midterm_exam;
	}
	public int getFinal_exam() {
		return final_exam;
	}
	public void setFinal_exam(int final_exam) {
		this.final_exam = final_exam;
	}
}
