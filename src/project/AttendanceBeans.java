package project;

public class AttendanceBeans {
	private int studentID;
	private String name;
	private int class_unique_number;
	private int division;
	private int number_of_times;
	
	public int getStudentID() {
		return studentID;
	}
	public void setStudentID(int studentID) {
		this.studentID = studentID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getClass_unique_number() {
		return class_unique_number;
	}
	public void setClass_unique_number(int class_unique_number) {
		this.class_unique_number = class_unique_number;
	}
	public int getDivision() {
		return division;
	}
	public void setDivision(int division) {
		this.division = division;
	}
	public int getNumber_of_times() {
		return number_of_times;
	}
	public void setNumber_of_times(int number_of_times) {
		this.number_of_times = number_of_times;
	}
}
